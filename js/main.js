// Methodology flow intersection observer: reveal steps when visible
(function () {
    'use strict';
    try {
        var steps = document.querySelectorAll('.method-step');
        if (!steps || steps.length === 0) return;
        var io = new IntersectionObserver(function (entries) {
            entries.forEach(function (entry) {
                if (entry.isIntersecting) {
                    entry.target.classList.add('is-visible');
                    io.unobserve(entry.target);
                }
            });
        }, { threshold: 0.15 });
        steps.forEach(function (el) { io.observe(el); });
    } catch (e) {
        // IntersectionObserver not supported — show steps immediately
        var s = document.querySelectorAll('.method-step');
        s.forEach(function (el) { el.classList.add('is-visible'); });
    }
})();

